package com.example.asad.volontaireos;

public class Infodata {
String infoID;
String infoName;

    public Infodata(String infoID, String infoName) {
        this.infoID = infoID;
        this.infoName = infoName;
    }

    public String getInfoID() {
        return infoID;
    }

    public String getInfoName() {
        return infoName;
    }
}
